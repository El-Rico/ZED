#ifndef __ZEDRENDERTYPES_XBOX_HPP__
#define __ZEDRENDERTYPES_XBOX_HPP__

// Format types for the different buffers, etc.
typedef enum _ZED_FORMAT
{
	// Colour
	ZED_FORMAT_ARGB8	=	0x00000001,
	ZED_FORMAT_XRGB8	=	0x00000002,
	ZED_FORMAT_RGB565	=	0x00000003,
	ZED_FORMAT_ARGB1555 =	0x00000004,

	// Depth/Stencil
	ZED_FORMAT_D24S8 =		D3DFMT_D24S8,

	// Hope to hell that nothing takes this (other than more undefined formats)
	ZED_FORMAT_UNDEFINED = 0x7FFFFFFF
} ZED_FORMAT;

// Types to use for a shader [Vertex, Fragment, and Geometry]
typedef enum __ZED_SHADER_INPUT_TYPE
{
	ZED_FLOAT1		= 0x00000001,
	ZED_FLOAT2		= 0x00000002,
	ZED_FLOAT3		= 0x00000003,
	ZED_FLOAT4		= 0x00000004,

	ZED_INT1		= 0x00000005,
	ZED_INT2		= 0x00000006,
	ZED_INT3		= 0x00000007,
	ZED_INT4		= 0x00000008,

	ZED_MAT2X2		= 0x00000009,
	ZED_MAT3X3		= 0x0000000A,
	ZED_MAT4X4		= 0x0000000B,
}ZED_SHADER_INPUT_TYPE;


typedef enum __ZED_SHADER_TYPE
{
	ZED_VERTEX_SHADER	= 0x00000001,
	ZED_FRAGMENT_SHADER	= 0x00000002,
	ZED_UNKNOWN_SHADER	= 0xFFFFFFFF
}ZED_SHADER_TYPE;

// The Xbox version is more esoteric than the default D3D8 MSAA 2-16
typedef enum __ZED_SAMPLE_TYPE
{
	ZED_SAMPLE_TYPE_NONE					= 0x00000001,
	ZED_SAMPLE_TYPE_XBOX_MSAA_2_LINEAR		= 0x00000002,
	ZED_SAMPLE_TYPE_XBOX_MSAA_2_QUINCUX		= 0x00000003,
	ZED_SAMPLE_TYPE_XBOX_SSAA_HORIZ_LINEAR	= 0x00000004,
	ZED_SAMPLE_TYPE_XBOX_SSAA_VERT_LINEAR	= 0x00000005,
	ZED_SAMPLE_TYPE_XBOX_MSAA_4_LINEAR		= 0x00000006,
	ZED_SAMPLE_TYPE_XBOX_MSAA_4_GAUSSIAN	= 0x00000007,
	ZED_SAMPLE_TYPE_XBOX_SSAA_4_LINEAR		= 0x00000008,
	ZED_SAMPLE_TYPE_XBOX_SSAA_4_GAUSSIAN	= 0x00000009,
	ZED_SAMPLE_TYPE_XBOX_MSAA_9_GAUSSIAN	= 0x0000000A,
	ZED_SAMPLE_TYPE_XBOX_SSAA_9_GAUSSIAN	= 0x0000000B,
	ZED_SAMPLE_TYPE_XBOX_PF_DEFAULT			= 0x0000000C,
	ZED_SAMPLE_TYPE_XBOX_PF_X1RGB5			= 0x0000000D,
	ZED_SAMPLE_TYPE_XBOX_PF_XRGB8			= 0x0000000E,
	ZED_SAMPLE_TYPE_XBOX_PF_ARGB8			= 0x0000000F,
	ZED_SAMPLE_TYPE_UNKNOWN					= 0x7FFFFFFF,
}ZED_SAMPLE_TYPE;

typedef enum __ZED_SAMPLE_MODE
{
	ZED_SAMPLE_MODE_1	= 0x00000001,
	ZED_SAMPLE_MODE_2	= 0x00000002,
	ZED_SAMPLE_MODE_4	= 0x00000003,
	ZED_SAMPLE_UNKNOWN	= 0x7FFFFFFF,
}ZED_SAMPLE_MODE;

#endif
